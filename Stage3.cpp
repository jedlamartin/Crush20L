static float matrixMul(const std::array<float, 4>& A,
                       const std::array<float, 4>& B) {
    return A[0] * B[0] + A[1] * B[1] + A[2] * B[2] + A[3] * B[3];
}

static std::array<float, 4> matrixMul(const std::array<float, 4>& A, float B) {
    std::array<float, 4> C {0.0f};
    for(int i = 0; i < A.size(); i++) {
        C[i] = B * A[i];
    }
    return C;
}

static std::array<float, 4> matrixAdd(const std::array<float, 4>& A,
                                      const std::array<float, 4>& B) {
    std::array<float, 4> C;
    C[0] = A[0] + B[0];
    C[1] = A[1] + B[1];
    C[2] = A[2] + B[2];
    C[3] = A[3] + B[3];
    return C;
}

